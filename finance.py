import pandas as pd
import numpy as np
from xgboost import XGBRegressor
from sklearn.metrics import mean_squared_error

# ── Load engineered data ──────────────────────────────────────

stocks = pd.read_csv("engineered_stocks.csv")
stocks["Date"] = pd.to_datetime(stocks["Date"])
stocks = stocks.sort_values(by=["Company", "Date"]).reset_index(drop=True)

# ── Create next-day target per company ────────────────────────
# This means each row's features are used to predict the next day's Close

stocks["Target"] = stocks.groupby("Company")["Close"].shift(-1)

# Remove rows with missing target values
stocks = stocks.dropna(subset=["Target"]).reset_index(drop=True)

# ── Define features and target ────────────────────────────────

FEATURES = [
    "Open", "High", "Low", "Volume",
    "Daily_Return", "Log_Return",
    "MA_7", "MA_30",
    "EMA_12", "EMA_26", "MACD",
    "Volatility_7", "Volatility_30",
    "BB_Upper", "BB_Lower", "BB_Width",
    "RSI_14",
    "Momentum_7", "Momentum_30",
    "Volume_MA_7", "Volume_Ratio",
    "Close_Lag_1", "Close_Lag_3", "Close_Lag_7"
]

TARGET = "Target"

# ── Train and evaluate per company ───────────────────────────

results = {}
models = {}
importance_dfs = []

for company, group in stocks.groupby("Company"):

    # Keep time order
    group = group.sort_values("Date").reset_index(drop=True)
    split_idx = int(len(group) * 0.8)

    train = group.iloc[:split_idx]
    test = group.iloc[split_idx:]

    X_train = train[FEATURES]
    y_train = train[TARGET]
    X_test = test[FEATURES]
    y_test = test[TARGET]

    # Train XGBoost model
    model = XGBRegressor(
        n_estimators=500,
        learning_rate=0.05,
        max_depth=5,
        subsample=0.8,
        colsample_bytree=0.8,
        random_state=42,
        verbosity=0
    )

    model.fit(
        X_train,
        y_train,
        eval_set=[(X_test, y_test)],
        verbose=False
    )

    # Predict and evaluate
    predictions = model.predict(X_test)
    rmse = np.sqrt(mean_squared_error(y_test, predictions))

    results[company] = {
        "RMSE": round(rmse, 4),
        "y_test": y_test.values,
        "predictions": predictions,
        "dates": test["Date"].values
    }
    models[company] = model

    # Feature importance
    imp_df = pd.DataFrame({
        "Company": company,
        "Feature": FEATURES,
        "Importance": model.feature_importances_
    })
    importance_dfs.append(imp_df)

    print(f"{company:6s} | RMSE: ${rmse:.4f}")

# ── RMSE summary across all companies ─────────────────────────

print("\n── RMSE Summary ─────────────────────────────────────")
rmse_summary = pd.DataFrame(
    {
        "Company": list(results.keys()),
        "RMSE ($)": [results[c]["RMSE"] for c in results]
    }
).sort_values("RMSE ($)")

print(rmse_summary.to_string(index=False))

# ── Feature importance summary ────────────────────────────────

print("\n── Top 5 Features per Company ───────────────────────")
all_importance = pd.concat(importance_dfs, ignore_index=True)
all_importance["Importance"] = (all_importance["Importance"] * 100).round(2)

for company, group in all_importance.groupby("Company"):
    top5 = group.sort_values("Importance", ascending=False).head(5)
    top5 = top5.rename(columns={"Importance": "Importance (%)"})
    print(f"\n{company}:")
    print(top5[["Feature", "Importance (%)"]].to_string(index=False))

# ── Overall feature importance ────────────────────────────────

print("\n── Overall Feature Importance (avg across MAG7) ─────")
avg_importance = (
    all_importance.groupby("Feature")["Importance"]
    .mean()
    .sort_values(ascending=False)
    .reset_index()
)
avg_importance.columns = ["Feature", "Avg_Importance (%)"]
avg_importance["Avg_Importance (%)"] = avg_importance["Avg_Importance (%)"].round(2)

print(avg_importance.to_string(index=False))

# ── Save outputs ──────────────────────────────────────────────

rmse_summary.to_csv("rmse_summary.csv", index=False)
all_importance.to_csv("feature_importance.csv", index=False)
avg_importance.to_csv("avg_feature_importance.csv", index=False)

pred_rows = []
for company, data in results.items():
    for date, actual, predicted in zip(data["dates"], data["y_test"], data["predictions"]):
        pred_rows.append({
            "Company": company,
            "Date": date,
            "Actual": actual,
            "Predicted": predicted
        })

predictions_df = pd.DataFrame(pred_rows)
predictions_df.to_csv("predictions.csv", index=False)

