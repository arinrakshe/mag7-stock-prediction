import pandas as pd
import numpy as np

# Load cleaned data
stocks = pd.read_csv("cleaned_stocks.csv")
stocks["Date"] = pd.to_datetime(stocks["Date"])
stocks = stocks.sort_values(by=["Company", "Date"]).reset_index(drop=True)


# ── Helper functions ──────────────────────────────────────────

def add_pct_change(group):
    group["Daily_Return"] = group["Close"].pct_change()
    return group

def add_log_return(group):
    group["Log_Return"] = np.log(group["Close"] / group["Close"].shift(1))
    return group

def add_moving_averages(group):
    group["MA_7"]  = group["Close"].rolling(window=7).mean()
    group["MA_30"] = group["Close"].rolling(window=30).mean()
    return group

def add_ema(group):
    group["EMA_12"] = group["Close"].ewm(span=12, adjust=False).mean()
    group["EMA_26"] = group["Close"].ewm(span=26, adjust=False).mean()
    group["MACD"]   = group["EMA_12"] - group["EMA_26"]
    return group

def add_volatility(group):
    group["Volatility_7"]  = group["Daily_Return"].rolling(window=7).std()
    group["Volatility_30"] = group["Daily_Return"].rolling(window=30).std()
    return group

def add_bollinger_bands(group):
    rolling_mean = group["Close"].rolling(window=20).mean()
    rolling_std  = group["Close"].rolling(window=20).std()
    group["BB_Upper"] = rolling_mean + (2 * rolling_std)
    group["BB_Lower"] = rolling_mean - (2 * rolling_std)
    group["BB_Width"] = group["BB_Upper"] - group["BB_Lower"]
    return group

def compute_rsi(series, window=14):
    delta    = series.diff()
    gain     = delta.clip(lower=0)
    loss     = -delta.clip(upper=0)
    avg_gain = gain.ewm(com=window - 1, min_periods=window).mean()
    avg_loss = loss.ewm(com=window - 1, min_periods=window).mean()
    rs       = avg_gain / avg_loss
    return 100 - (100 / (1 + rs))

def add_rsi(group):
    group["RSI_14"] = compute_rsi(group["Close"])
    return group

def add_momentum(group):
    group["Momentum_7"]  = group["Close"] - group["Close"].shift(7)
    group["Momentum_30"] = group["Close"] - group["Close"].shift(30)
    return group

def add_volume_features(group):
    group["Volume_MA_7"]  = group["Volume"].rolling(window=7).mean()
    group["Volume_Ratio"] = group["Volume"] / group["Volume_MA_7"]
    return group

def add_lag_features(group):
    group["Close_Lag_1"] = group["Close"].shift(1)
    group["Close_Lag_3"] = group["Close"].shift(3)
    group["Close_Lag_7"] = group["Close"].shift(7)
    return group


# ── Apply all feature functions ───────────────────────────────

def _apply_per_company(df, fn):
    parts = [fn(g.copy()) for _, g in df.groupby("Company", sort=False)]
    return pd.concat(parts).sort_values(["Company", "Date"]).reset_index(drop=True)

for _fn in [add_pct_change, add_log_return, add_moving_averages, add_ema,
            add_volatility, add_bollinger_bands, add_rsi, add_momentum,
            add_volume_features, add_lag_features]:
    stocks = _apply_per_company(stocks, _fn)

# Drop NaN rows introduced by rolling/lag calculations
stocks = stocks.dropna().reset_index(drop=True)


